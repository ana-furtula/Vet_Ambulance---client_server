    private void btnStartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnStartActionPerformed
        if (serverThread == null || !serverThread.isAlive()) {
            try {
                serverThread = new ServerThread();
                serverThread.start();
                this.setTitle("Server (pokrenut)");
                serverThread.setLoggingListener(new LoggingListener() {
                    @Override
                    public void loginHappened(Employee employee) {
                        TableModelLoggedUsers model = (TableModelLoggedUsers) tblLoggedEmployees.getModel();
                        model.addEmployee(employee);
                    }

                    @Override
                    public void logoutHappened(Employee employee) {
                        TableModelLoggedUsers model = (TableModelLoggedUsers) tblLoggedEmployees.getModel();
                        model.removeEmployee(employee);
                    }
                });
                btnStart.setEnabled(false);
                btnStop.setEnabled(true);
            } catch (Exception ex) {
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnStartActionPerformed

    private void btnStopActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnStopActionPerformed
        if (serverThread.getServerSocket() != null && serverThread.getServerSocket().isBound()) {
            try {
                serverThread.stopAllThreads();
                TableModelLoggedUsers model = (TableModelLoggedUsers) tblLoggedEmployees.getModel();
                model.updateTable(serverThread.getLoggedInEmployees());
                serverThread.getServerSocket().close();
                this.setTitle("Server");
                btnStart.setEnabled(true);
                btnStop.setEnabled(false);
            } catch (IOException ex) {
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnStopActionPerformed

