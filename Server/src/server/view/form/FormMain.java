    private void prepareView() {
        btnStop.setEnabled(false);
        tblLoggedEmployees.setModel(new TableModelLoggedUsers(new LinkedList<>()));
        tblLoggedEmployees.getTableHeader().setOpaque(false);
        tblLoggedEmployees.getTableHeader().setBackground(new Color(51, 153, 255));
        tblLoggedEmployees.getTableHeader().setForeground(new Color(255, 255, 255));
        DefaultTableCellRenderer renderer = (DefaultTableCellRenderer) tblLoggedEmployees.getDefaultRenderer(Object.class);
        renderer.setHorizontalAlignment(JLabel.LEFT);
        tblLoggedEmployees.setRowHeight(22);
    }
